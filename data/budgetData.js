const enrichDepartment = (department) => {
  const totalBudget = department.personnelBudget + department.operatingBudget + department.capitalBudget;
  const averageFteCost = department.fteCount > 0 ? Math.round(department.personnelBudget / department.fteCount) : 0;
  const fteProfile = activeFteDepartments.find((item) => item.id === department.id);

  return {
    ...department,
    totalBudget,
    averageFteCost,
    historicalFte: fteProfile ? fteProfile.historicalFte : null,
    nonFteAdjustable: department.fteCount === 0 || nonFteAdjustableDepartmentIds.includes(department.id)
  };
};

